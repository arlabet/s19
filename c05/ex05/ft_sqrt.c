int		ft_sqrt(int nb)
{
	int x;

	x = 2;
	if (nb < 0)
		return (0);
	if (nb == 1 || nb == 0)
		return (nb);
	if (nb == 4)
		return (2);
	while (x <= nb / x)
	{
		if (x * x == nb)
			return (x);
		x++;
	}
	return (0);
}
