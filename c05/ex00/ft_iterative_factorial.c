int		ft_iterative_factorial(int nb)
{
	int f;

	f = nb;
	if (nb < 0)
		return (0);
	if (nb == 0 || nb == 1)
		return (1);
	while (nb > 1)
	{
		nb--;
		f = f * nb;
	}
	return (f);
}
