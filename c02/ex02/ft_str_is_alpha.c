int		ft_str_is_alpha(char *str)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (str[i])
	{
		if ((str[i] >= 'a' && str[i] <= 'z') ||
			(str[i] >= 'A' && str[i] <= 'Z'))
			j++;
		i++;
	}
	if ((i == j) || (i == 0))
		return (1);
	else
		return (0);
}
